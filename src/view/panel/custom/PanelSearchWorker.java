    public void preparePanel() {
        tmr = new TableModelRadnik(radnici);
        panelSearchWorker.preparePanel(tmr);
        panelSearchWorker.addListener(this);
        panelSearchWorker.addMyTableListener(this);
        panelSearchWorker.setPanelTitle("Pretra≈æi radnike");
    }

    public void clearPanel() {
        radnici.clear();
        panelSearchWorker.clearPanel(new TableModelRadnik(radnici));
    }

    @Override
    public AbstractTableModel searchOdo(String criteria) throws Exception {
        try {
            radnici = CommunicationController.getInstance().searchWorkerWithCriteria(criteria);
            JOptionPane.showMessageDialog(this, "Uspesno vraceni radnici!", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
            return new TableModelRadnik(radnici);
        } catch (Exception ex) {
            Logger.getLogger(PanelSearchWorker.class.getName()).log(Level.SEVERE, null, ex);
            throw ex;
        }
    }

    @Override
    public void clickedRow(int clickedRow) {
        radnik = tmr.getWorker(clickedRow);
    }

    @Override
    public Object getValue() {
        return radnik;
    }

    @Override
    public void setValue(Object object) {
        this.radnik = (Radnik) object;
    }

    private void fillTableData() {
        try {
            radnici = CommunicationController.getInstance().selectAllWorkers();
        } catch (Exception ex) {
            Logger.getLogger(PanelSearchWorker.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Override
    public void removeObject(DomainObject odo) throws Exception {

        radnik = (Radnik) getValue();
        CommunicationController.getInstance().deleteObject(radnik);
        radnici = CommunicationController.getInstance().selectAllWorkers();
        tmr.azuriraj(radnici);

    }

    @Override
    public void updateTable() throws Exception {
        radnici = CommunicationController.getInstance().selectAllWorkers();
        tmr.azuriraj(radnici);
    }
